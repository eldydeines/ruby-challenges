#case practice
weather = "sunny"

case (weather)
	when "sunny" then puts "It's sunny. You may need some sunscreen today."
	when "cloudy" then puts "It might be a little dreary since it's cloudy out."
	when "foggy" then puts "It's low visibility today.  Be careful out there!"
	when "rainy" then puts "There is some rain expected so you should bring an umbrella."
	when "snowing" then puts "It's snowing today so get a warm jacket."
	else puts "Not sure what's out there today!"
end

